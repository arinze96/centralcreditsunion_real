                <h2>A family-owned bank proudly serving Texans for over fifty years.</h2>
                <p>Established in 1964 in a small East Texas town, Quinlan State Bank was the long-time dream of retired oil man Bruce Clardy. Clardy served as Chairman of the Board of Quinlan State Bank, located near his ranch about 45 miles east of Dallas, until his grandson and current Chairman Michael A. Barnett took over in 1983.</p>
                <p>In 1987, Barnett bought a failed bank in the Dallas-area and renamed the institution Benchmark Bank. Barnett grew the bank to include 9 branches expanding from East Texas to the Park Cities. He also opened a title company, Benchmark Title Services, in 1998 to more completely serve customer needs.</p>
                <p>In 2002, Barnett sold all of the bank branches except one, and Benchmark opened its current headquarters in The Shops at Legacy in Plano. A few years later, a branch in the Park Cities, on the corner of Hillcrest and Lovers Lane in Snider Plaza, was added. In an effort to further meet the demands of a growing customer base, Benchmark Bank opened a branch on McKinney Avenue in Uptown Dallas in 2012. That same year Benchmark Bank expanded to Austin at the corner of Lamar and MLK and, in 2013, a second branch was opened in Austin on W. 35th Street. Westlake&#x27;s opening in early 2017 marked the third branch for the Austin area. Under the guidance of CEO Jonathan Filgo and Bank President Bill Brewer, our fourth branch in Dallas opened in the Spring of 2019 at the corner of Preston and Royal in North Dallas. Greater Houston is the newest market for Benchmark Bank with The Woodlands opening in 2015 and the Houston metro area slated to open in the Fall of 2019.</p>
                <p>Today, in addition to the retail bank branches, the Benchmark family includes Benchmark Title and Benchmark Private Wealth Management.</p>
                <p>Benchmark Title has offices in Uptown Dallas, Plano, the Park Cities, and Austin for closing commercial and residential transactions all over North America with over 50 employees, ten closing teams, five title attorneys and its own examination department. <a href="https://www.bmktitle.com/">Learn more about Benchmark Title.</a></p>
